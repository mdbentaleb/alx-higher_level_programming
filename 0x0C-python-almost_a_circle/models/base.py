    @staticmethod
    def draw(list_rectangles, list_squares):
        import turtle
        import time
        from random import randrange
        turtle.Screen().colormode(255)
        for j in list_rectangles + list_squares:
            t = turtle.Turtle()
            t.color((randrange(255), randrange(255), randrange(255)))
            t.pensize(1)
            t.penup()
            t.pendown()
            t.setpos((j.x + t.pos()[0], j.y - t.pos()[1]))
            t.pensize(10)
            t.forward(j.width)
            t.left(90)
            t.forward(j.height)
            t.left(90)
            t.forward(j.width)
            t.left(90)
            t.forward(j.height)
            t.left(90)
            t.end_fill()

        time.sleep(5)
